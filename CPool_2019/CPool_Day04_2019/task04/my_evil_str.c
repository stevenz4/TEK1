char *my_evil_str(char *str)
{
    int charac;

    for ( charac = 0; str[charac] != '\0'; charac++)
    for ( int reverse = charac; reverse != -1; reverse--)
        return str;
        return 0;
}

int main(void)
{
    my_evil_str("hello");
    return 0;
}

  
    
